#include "pipex.h"

void	gls_free_tbl(char **tbl)
{
	int	i;

	if (tbl == NULL)
		return ;
	i = 0;
	while (tbl[i] != NULL)
	{
		free(tbl[i]);
		i++;
	}
	free(tbl);
}
